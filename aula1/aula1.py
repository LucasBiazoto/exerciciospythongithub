def misturar_dicionarios(d1, d2):
    resultado = d1.copy()  # Começa com os dados do primeiro dicionário
    for chave, valor in d2.items():
        if chave in resultado:
            resultado[chave] += valor  # Soma os valores se a chave já existir
        else:
            resultado[chave] = valor  # Adiciona nova chave
    return resultado

